    private void tkmabcKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tkmabcKeyReleased
        // TODO add your handling code here:
        try {
           // Lấy mã bệnh nhân từ trường nhập liệu
           tbxv.removeAll();
           String mbc = tkmabc.getText().trim();  // Mã báo cáo

           // Kết nối đến cơ sở dữ liệu
           con = BTL.Connect.KetnoiDB();
           Statement st = con.createStatement();

           // Xây dựng câu lệnh SQL cho tìm kiếm
           String sql = "SELECT * FROM BaoCaoBenhNhanDaXuatVien WHERE MaBaoCao LIKE '%" + mbc + "%'";

           // Thực hiện truy vấn
           ResultSet rs = st.executeQuery(sql);
           String[] tieude = {"Mã Báo Cáo", "Mã Bệnh Nhân", "Ngày Xuất Viện","Kết Qủa Điều Trị"};
           DefaultTableModel tb = new DefaultTableModel(tieude, 0);
           tb.setRowCount(0);

           // Duyệt qua kết quả và thêm vào bảng
           while (rs.next()) {
               Vector<String> v = new Vector<>();
               v.add(rs.getString("MaBaoCao"));          // Mã báo cáo
               v.add(rs.getString("MaBenhNhan"));         // Mã bệnh nhân
               v.add(rs.getString("NgayXuatVien"));        // Ngày đxuất viện
               v.add(rs.getString("KetQuaDieuTri"));       //kết quả điều trị
               tb.addRow(v);
           }

           // Cập nhật bảng hiển thị
           tbxv.setModel(tb);
           con.close();  // Đóng kết nối
       } catch (Exception e) {
           e.printStackTrace();
           JOptionPane.showMessageDialog(this, "Tìm kiếm không thành công");
       }
    }//GEN-LAST:event_tkmabcKeyReleased

