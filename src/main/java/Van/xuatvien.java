    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
        try {
        String kq = txtkq.getText();  // kết quả điều trị

        // Kiểm tra nếu các trường không được để trống
        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống kết quả điều trị");
            return;
        }
        

        // Lấy ngày điều trị từ DateChooser
        java.sql.Date ndt = new java.sql.Date (dcnxv.getDate().getTime());
        if (ndt == null) {
            JOptionPane.showMessageDialog(this, "Không được để trống ngày xuất viện!");
            return;
        }

        // Định dạng ngày điều trị thành kiểu chuỗi
        java.sql.Date sqlDate = new java.sql.Date(ndt.getTime());

        // Lấy mã điều trị và mã bệnh nhân (giả sử bạn có trường txtmdt và txtmbn để nhập mã)
        String mbc = txtmabc.getText();  // Mã báo cáo
        String mbn = txtmbn.getText();  // Mã bệnh nhân

        // Kiểm tra mã điều trị không được trống
        if (mbn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã bệnh nhân");
            return;
        }

        con = BTL.Connect.KetnoiDB();  // Kết nối tới database

        // Câu lệnh SQL để cập nhật bản ghi trong bảng QuaTrinhDieuTri
        String sql = "UPDATE BaoCaoBenhNhanDaXuatVien SET "
                   + "NgayXuatVien = ?, "
                   + "KetQuaDieuTri = ?, "
                   + "WHERE MaBenhNhan = ? AND MaBaoCao = ?";

        // Sử dụng PreparedStatement để tránh SQL Injection
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setDate(1, sqlDate);  // Gán ngày xuất viện
        ps.setString(2, kq);    //gán kết quả điều trị
        ps.setString(3, mbc);    // Gán mã báo cáo
        ps.setString(4, mbn);    // Gán mã bệnh nhân

        // Thực hiện câu lệnh cập nhật
        int rowsAffected = ps.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadxuatvien();  // Tải lại dữ liệu sau khi cập nhật
            xoatrang();  
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy dữ liệu để sửa");
        }
      
        con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa không thành công");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btupdateActionPerformed

