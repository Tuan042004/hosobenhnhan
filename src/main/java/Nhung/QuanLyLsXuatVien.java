    private void tbLSXVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbLSXVMouseClicked
        // TODO add your handling code here:
        int i = tbLSXV.getSelectedRow();
        if (i != -1) {
            DefaultTableModel model = (DefaultTableModel) tbLSXV.getModel();
            mxv.setText(model.getValueAt(i, 0).toString());
            mbn.setText(model.getValueAt(i, 1).toString());
            String ngay = model.getValueAt(i, 2).toString();
            java.util.Date ngaysinh;
            try {
                ngaysinh = new SimpleDateFormat("yyyy-MM-dd").parse(ngay);
                ngayxv.setDate(ngaysinh);
            } catch (Exception e) {
                e.printStackTrace();
            }
            kqdt.setText(model.getValueAt(i, 3).toString());
            gc.setText(model.getValueAt(i, 4).toString());

            mxv.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hồ sơ để xem thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_tbLSXVMouseClicked
