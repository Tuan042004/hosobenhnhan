    private void load_LS(){
        try {
            tbLSXV.removeAll(); 
            con = BTL.Connect.KetnoiDB();
            String sql = "SELECT * FROM LichSuXuatVien";
            Statement st = con.createStatement(); 
            ResultSet rs = st.executeQuery(sql); 
            String[] tieude = {"Mã xuất viện", "Mã bệnh nhân", "Ngày xuất viện", "Kết quả điều trị","Ghi chú"};
            DefaultTableModel tb = new DefaultTableModel(tieude, 0);

            while (rs.next()) {
                Vector<String> v = new Vector<>();
                v.add(rs.getString("MaXuatVien")); 
                v.add(rs.getString("MaBenhNhan")); 
                v.add(rs.getDate("NgayXuatVien") != null ? rs.getDate("NgayXuatVien").toString() : ""); // Ngày nhập viện
                v.add(rs.getString("KetQuaDieuTri"));
                v.add(rs.getString("GhiChu")); 
                tb.addRow(v); 
            }
            tbLSXV.setModel(tb); 
            con.close(); 
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
