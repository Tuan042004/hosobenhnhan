    private void timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timkiemActionPerformed
            // TODO add your handling code here:
             String searchTerm = tk_ma.getText().trim(); // Lấy mã xuất viện từ JTextField

        if (searchTerm.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã xuất viện để tìm kiếm!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return; // Dừng lại nếu mã xuất viện trống
        }

        Connection con = null;
        PreparedStatement pst = null;
        ResultSet rs = null;

        try {
            con = BTL.Connect.KetnoiDB(); // Kết nối cơ sở dữ liệu
            String sql = "SELECT * FROM LichSuXuatVien WHERE MaXuatVien = ?"; // Câu lệnh SQL tìm theo MaXuatVien
            pst = con.prepareStatement(sql);
            pst.setString(1, searchTerm); // Đưa mã xuất viện vào câu truy vấn

            rs = pst.executeQuery(); // Thực hiện câu truy vấn

            if (rs.next()) {
                mxv.setText(rs.getString("MaXuatVien")); // Hiển thị mã xuất viện
                mbn.setText(rs.getString("MaBenhNhan"));  // Hiển thị mã bệnh nhân
                Date ngayXuatVien = rs.getDate("NgayXuatVien"); // Hiển thị ngày xuất viện
                if (ngayXuatVien != null) {
                    ngayxv.setDate(ngayXuatVien);
                }
                kqdt.setText(rs.getString("KetQuaDieuTri")); // Hiển thị kết quả điều trị
                gc.setText(rs.getString("GhiChu")); // Hiển thị ghi chú
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy hồ sơ xuất viện với mã đã cho!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi SQL: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Đóng các tài nguyên (ResultSet, PreparedStatement, Connection)
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_timkiemActionPerformed

