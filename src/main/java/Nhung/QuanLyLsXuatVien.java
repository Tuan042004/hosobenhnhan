    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        // TODO add your handling code here:
        String maxv = this.mxv.getText().trim();
        String mabn = mbn.getText().trim();
        Date ngay= new Date(ngayxv.getDate().getTime());
        if (ngayxv.getDate() != null) {
            ngay = new Date(ngayxv.getDate().getTime());
        }
        String kq = kqdt.getText().trim();
        String gchu = gc.getText().trim();
        
        if (maxv.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã xuất viện không được để trống.");
            mxv.requestFocus();
            return;
        }

        if (mabn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã bệnh nhân không được để trống.");
            mbn.requestFocus();
            return;
        }

        if (ngay == null) {
            JOptionPane.showMessageDialog(this, "Ngày xuất viện không được để trống.");
            ngayxv.requestFocus();
            return;
        }

        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kết quả điều trị không được để trống.");
            kqdt.requestFocus();
            return;
        }

        if (gchu.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ghi chú không được để trống.");
            gc.requestFocus();
            return;
        }
        
        try {
            con = BTL.Connect.KetnoiDB();
            // Kiểm tra xem kết nối có null không
            if (con == null) {
                JOptionPane.showMessageDialog(this, "Không thể kết nối đến cơ sở dữ liệu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return; // Thoát khỏi phương thức nếu không kết nối được
            }
            String sql = "INSERT INTO LichSuXuatVien " +
            "VALUES ('"+ maxv +"', '" + mabn + "', '" + ngay +"', N'"+kq+"', N'"+gchu+"')";

            // Tạo Statement
            Statement st = con.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(this, "Lịch sử xuất viện đã được thêm thành công!");
            st.close();
            con.close();
            load_LS();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_themActionPerformed

